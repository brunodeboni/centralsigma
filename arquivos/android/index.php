	$('#form_promo').submit();
});

function checarEmail(mail){
	if(mail.length==0) return true;
	
	if ((mail.length > 7) && !((mail.indexOf("@") < 1) || (mail.indexOf('.') < 2)))
		{return true;}
	else
		{return false;}
}

function checarPerfil(perfil) {
	$.post('ajax_perfil.php', {perfil: perfil}, function(data) {
		if (data == 'true') return true;
		else return false;
	});
}
