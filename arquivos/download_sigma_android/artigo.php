<h1 style="font-size: 18px;">COMECE A USAR!</h1>

<p>Os primeiros 30 dias de uso do SIGMA ANDROID serão gratuitos, de forma a possibilitar que sua empresa faça um ‘test-drive’ no sistema, antes da aquisição.</p>
<img src="/arquivos/android/img/sigma-android-precos.png" width="480" style="margin-left: 120px; border: 0;">
<br />

<p>Para começar a usar é muito simples, basta seguir o passo-a-passo abaixo:</P>
<p><b>Passo 1:</b> Preencha o cadastro abaixo com as informações de sua empresa e aparecerá o botão para download do aplicativo;</p>
<p><b>Passo 2:</b> Com o arquivo baixado em seu PC, vá até seu smartphone Android e acesse as configurações do sistema;</p>
<p><b>Passo 3:</b> Toque no item de menu “Aplicações” e, na tela que for exibida, marque a opção “Fontes desconhecidas”. Ou seja, seu smartphone agora permitirá a instalação de aplicativos por fora do Google Play;</p>
<p><b>Passo 4:</b> Volte novamente a tela inicial de configuração do Android e escolha a opção “Conexões sem fio e rede”, na tela seguinte, escolha a opção “Utilitários USB”;</p>
<p><b>Passo 5:</b> Plugue o cabo USB de seu dispositivo ao PC, e ainda no smartphone, toque no botão “Conectar memória ao PC”, em seguida, “Ligar armazenamento USB”;</p>
<p><b>Passo 6:</b> De volta ao PC, vá até a unidade de disco do dispositivo Android que acabou de ser montada em seu sistema, e “abra” a mesma;</p>
<p><b>Passo 7:</b> Na raiz do disco (ou em outro local preferido), crie uma pasta chamada “Meus aplicativos”;</p>
<p><b>Passo 8:</b> Vá até o local do sistema onde você baixou o arquivo de instalação com extensão APK e copie o mesmo para a pasta recém-criada;</p>
<p><b>Passo 9:</b> De volta ao smartphone, acesse o Explorer de arquivos de seu sistema.</p>
<p><b>Passo 10:</b> Com o Explorer aberto, acesse a raiz do sistema e procure a pasta “Meus aplicativos”. Lá, execute o arquivo Sigma Android.APK do seu aplicativo;</p>
<p><b>Passo 11:</b> Pronto! O Sigma Android será executado e o app, enfim, será instalado em seu smartphone.</p>
